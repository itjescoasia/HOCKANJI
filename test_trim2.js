suffixes.sort((a, b) => b.length - a.length);

function trimAuxiliary(text) {
  let changed = true;
  while (changed) {
    changed = false;
    for (const suffix of suffixes) {
      if (text.endsWith(suffix) && text.length > suffix.length) {
        text = text.substring(0, text.length - suffix.length);
        changed = true;
        break;
      }
    }
  }
  return text;
}

